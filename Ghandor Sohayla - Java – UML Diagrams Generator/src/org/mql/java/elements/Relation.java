package org.mql.java.elements;

public class Relation {
	
	private String name ;
	private Class<?> clas1 ;
	private Class<?> clas2 ;

	public Relation() {
		
	}

	public Relation(String name, Class<?> clas1, Class<?> clas2) {
		super();
		this.name = name;
		this.clas1 = clas1;
		this.clas2 = clas2;
	}
	
	
	

}
